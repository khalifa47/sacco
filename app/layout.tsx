import Providers from "@/app/(components)/layout/Providers";
import "./globals.css";
import { createServerComponentSupabaseClient } from "@supabase/auth-helpers-nextjs";
import { headers, cookies } from "next/headers";
import Header from "./(components)/layout/Header";
import { Notification } from "@prisma/client";

export const metadata = {
  title: "Kadhi Sacco",
  description: "Generated by create next app",
};

const getNotifications = async (uid: string) => {
  let res: Response;
  let notifications: Notification[] = [];

  try {
    res = await fetch(`${process.env.BASE_URL}/api/notifications?uid=${uid}`);
    if (!res.ok) {
      const msg = await res.text();
      throw new Error(msg === "" ? res.statusText : msg);
    }

    notifications = await res.json();
  } catch (error: any) {
    console.error(error);
  }

  return notifications;
};

export default async function RootLayout({
  children,
}: {
  children: React.ReactNode;
}) {
  let notifications: Notification[] = [];
  const supabase = createServerComponentSupabaseClient({
    headers,
    cookies,
  });

  const {
    data: { session },
  } = await supabase.auth.getSession();

  if (session !== null) {
    notifications = await getNotifications(session.user.id);
  }

  return (
    <html lang="en">
      <body style={{ margin: 0 }}>
        <Providers>
          {session && (
            <Header
              user={session.user.user_metadata}
              notifications={notifications}
            />
          )}

          {children}
        </Providers>
      </body>
    </html>
  );
}
